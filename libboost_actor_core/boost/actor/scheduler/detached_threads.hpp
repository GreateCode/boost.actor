#ifndef BOOST_ACTOR_SCHEDULER_DETACHED_THREADS_HPP
#define BOOST_ACTOR_SCHEDULER_DETACHED_THREADS_HPP

#include <cstddef>

namespace boost {
namespace actor {
namespace scheduler {

/// Increases count for detached threads by one.
void inc_detached_threads();

/// Decreases count for detached threads by one.
void dec_detached_threads();

/// Blocks the caller until all detached threads are done.
void await_detached_threads();

} // namespace scheduler
} // namespace actor
} // namespace boost

#endif // BOOST_ACTOR_SCHEDULER_DETACHED_THREADS_HPP

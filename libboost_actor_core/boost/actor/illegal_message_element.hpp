#ifndef BOOST_ACTOR_ILLEGAL_MESSAGE_ELEMENT_HPP
#define BOOST_ACTOR_ILLEGAL_MESSAGE_ELEMENT_HPP

#include <type_traits>

namespace boost {
namespace actor {

/// Marker class identifying classes in CAF that are not allowed
/// to be used as message element.
struct illegal_message_element {
  // no members (marker class)
};

template <class T>
struct is_illegal_message_element
  : std::is_base_of<illegal_message_element, T> { };

} // namespace actor
} // namespace boost

#endif // BOOST_ACTOR_ILLEGAL_MESSAGE_ELEMENT_HPP
